/** Returns the full language name for use in AI prompts */
export function getLangPromptInstruction(lang: string): string {
  if (!lang || lang === "en") return "";
  const name = LANG_NAMES[lang as LangCode];
  if (!name) return "";
  return `\n\nIMPORTANT: Write your entire response in ${name}. All headings, descriptions, tips, and recommendations must be in ${name}.`;
}
