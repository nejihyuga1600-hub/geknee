// ─── Browser language detection ───────────────────────────────────────────────

// Maps browser locale prefixes to our supported lang codes
const BROWSER_LANG_MAP: Record<string, LangCode> = {
  zh: "zh", "zh-cn": "zh", "zh-tw": "zh", "zh-hk": "zh",
  es: "es", "es-mx": "es", "es-ar": "es", "es-co": "es",
  pt: "pt", "pt-br": "pt", "pt-pt": "pt",
  ar: "ar", "ar-sa": "ar", "ar-eg": "ar", "ar-ae": "ar",
  fr: "fr", "fr-fr": "fr", "fr-ca": "fr", "fr-be": "fr",
  de: "de", "de-de": "de", "de-at": "de", "de-ch": "de",
  id: "id",
  it: "it",
  hi: "hi",
  ja: "ja",
  ms: "ms",
  pl: "pl",
  ru: "ru",
  ko: "ko",
};

export function detectBrowserLang(): LangCode | null {
  if (typeof navigator === "undefined") return null;
  const raw = (navigator.languages?.[0] ?? navigator.language ?? "en").toLowerCase();
  return BROWSER_LANG_MAP[raw] ?? BROWSER_LANG_MAP[raw.split("-")[0]] ?? null;
}

