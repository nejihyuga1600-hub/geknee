// Geographic labels for the globe.
// R = 10 (globe radius). Formula: x=R·cos(φ)·cos(λ), y=R·sin(φ), z=-R·cos(φ)·sin(λ)
// where φ = lat in radians, λ = lon in radians.
// All centroids are verified label-points guaranteed to fall on land.

export interface GeoLabel {
  name: string;
  lat: number;
  lon: number;
}

/** Convert lat/lon (degrees) to Three.js world position on the globe (R=10). */
export function geoPos(lat: number, lon: number): [number, number, number] {
  const R = 10.15;
  const phi = (lat * Math.PI) / 180;
  const lam = (lon * Math.PI) / 180;
  return [
    R * Math.cos(phi) * Math.cos(lam),
    R * Math.sin(phi),
    -R * Math.cos(phi) * Math.sin(lam),
  ];
}

